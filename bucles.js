/* Bucle while */
var contador = 0;
console.log('Bucle while');
while (contador <= 10) {
    console.log(contador);
    contador += 2;
}

/* Bucle for */
console.log('Bucle for');
for (var i = 0; i <= 20; i = i + 5) {
    console.log(i);
}

/* Bucle do white */
var sumar = 0;
console.log('Bucle do while');
do {
    console.log(sumar);
    sumar += 10;
} while (sumar <= 100);